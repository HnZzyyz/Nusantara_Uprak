    public void ambilDataProv(){
        Connection conn = Form.Koneksi.koneksiDB();
        String query = "SELECT * FROM provinces";
        try {
            PreparedStatement pst = conn.prepareStatement(query);
            ResultSet rs = pst.executeQuery();
            while (rs.next()) {
                PROV.addItem(rs.getString("name"));
                prov.add(new Provinces(rs.getInt("id"), rs.getString("name")));
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    public void ambilDataRegen(){
        Connection conn = Form.Koneksi.koneksiDB();
        String query = "SELECT * FROM regencies";
        try {
            PreparedStatement pst = conn.prepareStatement(query);
            ResultSet rs = pst.executeQuery();
            while (rs.next()) {
                regen.add(new Regencies(rs.getInt("id"),rs.getInt("province_id"),rs.getString("name")));
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    public void ambilDataDist(){
        Connection conn = Form.Koneksi.koneksiDB();
        String query = "SELECT * FROM districts";
        try {
            PreparedStatement pst = conn.prepareStatement(query);
            ResultSet rs = pst.executeQuery();
            while (rs.next()) {
                dist.add(new Distrik(rs.getInt("id"),rs.getInt("regency_id"),rs.getString("name")));
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }
