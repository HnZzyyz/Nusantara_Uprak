    private void PROVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PROVActionPerformed
        // TODO add your handling code here:                                  
    if (PROV.getSelectedIndex() > 0) {
        String selectedProv = PROV.getSelectedItem().toString();
        KAB.setEnabled(true);
        KAB.removeAllItems();
        for (Provinces prov : prov) {
            if (prov.getName().equals(selectedProv)) {
                for (Regencies r : regen) {
                    if (r.getProvince_id() == prov.getId()) {
                        KAB.addItem(r.getName());
                    }
                }
                break;
            }
        }
    } else {
        KAB.setEnabled(false);
        KEC.setEnabled(false);
        KEC.removeAllItems();
        KAB.removeAllItems();
    }
    }//GEN-LAST:event_PROVActionPerformed

    private void KABActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_KABActionPerformed
        // TODO add your handling code here:
        if (KAB.getSelectedIndex() > 0) {
            String selectedRegen = KAB.getSelectedItem().toString();
            KEC.enable(true);
            KEC.removeAllItems();
            for (Regencies prov : regen) {
                if (prov.getName().equals(selectedRegen)) {
                    for (Distrik r : dist) {
                        if (r.getRegerency_id()== prov.getId()) {
                            KEC.addItem(r.getName());
                        }
                    }
                    break;
                }
            }
        }
    }//GEN-LAST:event_KABActionPerformed

